// Чтобы запустить программу: go run main.go
package main // Объявляет основной пакет программы; обязательный для исполняемых Go-приложений

import "fmt" // Импортирует стандартную библиотеку fmt для форматированного ввода/вывода (например, печати в консоль)

func main() { // Определяет главную функцию — точку входа в программу
	fmt.Println("Start program...") // Выводит строку "Start program..." в консоль с новой строки

	intOne := 10         // Объявляет и инициализирует переменную intOne со значением 10; тип (int) определяется автоматически
	var intTwo int = 100 // Явно объявляет переменную intTwo типа int и присваивает ей значение 100

	fmt.Println("Auto определение типа данных:", intOne) // Выводит сообщение и значение переменной intOne
	fmt.Println("Явное указание типа данных:", intTwo)   // Выводит сообщение и значение переменной intTwo

	floatOne := 3.14                                       // Объявляет переменную floatOne со значением 3.14; тип автоматически определяется как float64
	var floatTwo float32 = 2.71                            // Явно объявляет переменную floatTwo типа float32 и присваивает значение 2.71
	fmt.Println("Auto определение типа данных:", floatOne) // Выводит значение floatOne (тип float64)
	fmt.Println("Явное указание типа данных:", floatTwo)   // Выводит значение floatTwo (тип float32)

	pi64 := 3.141592653589793            // Объявляет переменную pi64 с высокой точностью; тип — float64 (по умолчанию для дробных литералов)
	var pi32 float32 = 3.141592653589793 // Объявляет переменную pi32 типа float32; значение усекается до точности float32

	sumFloat64 := float64(pi32) + pi64 // Преобразует pi32 из float32 в float64 и складывает с pi64; результат — float64

	fmt.Println("float64(auto):", pi64)                 // Выводит значение pi64
	fmt.Println("float32(явно):", pi32)                 // Выводит значение pi32 (с меньшей точностью)
	fmt.Println("Сумма (после приведения)", sumFloat64) // Выводит результат сложения двух чисел после приведения типов
} // Завершает функцию main
