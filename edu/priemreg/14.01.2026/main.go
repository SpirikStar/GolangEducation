// Чтобы запустить программу: go run main.go
package main // Объявляет основной пакет программы; обязательный для исполняемых Go-приложений

import "fmt"     // Импортирует стандартную библиотеку fmt для форматированного ввода/вывода (например, печати в консоль)
import "strings" // Импортирует стандартную библиотеку strings для работы со строками (например, изменение регистра, поиск подстрок и т.д.)

func main() { // Определяет главную функцию — точку входа в программу
	fmt.Println("Start program...") // Выводит строку "Start program..." в консоль с новой строки

	intOne := 10         // Объявляет и инициализирует переменную intOne со значением 10; тип (int) определяется автоматически
	var intTwo int = 100 // Явно объявляет переменную intTwo типа int и присваивает ей значение 100

	fmt.Println("Auto определение типа данных:", intOne) // Выводит сообщение и значение переменной intOne
	fmt.Println("Явное указание типа данных:", intTwo)   // Выводит сообщение и значение переменной intTwo

	floatOne := 3.14            // Объявляет переменную floatOne со значением 3.14; тип автоматически определяется как float64
	var floatTwo float32 = 2.71 // Явно объявляет переменную floatTwo типа float32 и присваивает значение 2.71

	fmt.Println("Auto определение типа данных:", floatOne) // Выводит значение floatOne (тип float64)
	fmt.Println("Явное указание типа данных:", floatTwo)   // Выводит значение floatTwo (тип float32)

	pi64 := 3.141592653589793            // Объявляет переменную pi64 с высокой точностью; тип — float64 (по умолчанию для дробных литералов)
	var pi32 float32 = 3.141592653589793 // Объявляет переменную pi32 типа float32; значение усекается до точности float32

	sumFloat64 := float64(pi32) + pi64 // Преобразует pi32 из float32 в float64 и складывает с pi64; результат — float64

	fmt.Println("float64(auto):", pi64)                 // Выводит значение pi64
	fmt.Println("float32(явно):", pi32)                 // Выводит значение pi32 (с меньшей точностью)
	fmt.Println("Сумма (после приведения)", sumFloat64) // Выводит результат сложения двух чисел после приведения типов

	stringOne := "Хорошая погода!"
	// Объявляет и инициализирует переменную stringOne строковым значением на русском языке

	var stringTwo string = "Welcome home"
	// Явно объявляет переменную stringTwo типа string и присваивает ей английскую строку

	fmt.Println("Строка one:", stringOne)
	// Выводит метку и значение переменной stringOne

	fmt.Println("Строка two:", stringTwo)
	// Выводит метку и значение переменной stringOne

	fmt.Println("Длина строки (в байтах):", len(stringOne))
	// Выводит длину строки stringOne в байтах (а не в символах!); важно помнить, что для UTF-8 это может отличаться от количества букв

	fmt.Println("Строка one капсом:", strings.ToUpper(stringOne))
	// Преобразует строку stringOne в верхний регистр с учётом Unicode и выводит результат

	fmt.Println("Строка one нижний регистр:", strings.ToLower(stringOne))
	// Преобразует строку stringOne в нижний регистр с учётом Unicode и выводит результат

	boolOne := true
	// Объявляет логическую переменную boolOne со значением true

	var boolTwo bool = false
	// Явно объявляет логическую переменную boolTwo типа bool и присваивает false

	fmt.Println("Логический тип boolOne:", boolOne)
	// Выводит значение переменной boolOne

	fmt.Println("Логический тип boolTwo:", boolTwo)
	// Выводит значение переменной boolTwo

	nOne := 1
	// Объявляет целочисленную переменную nOne со значением 1 (тип int)

	nTwo := 1.5
	// Объявляет переменную nTwo со значением 1.5 (тип float64 по умолчанию)

	resultOne := float64(nOne) + nTwo
	// Преобразует nOne в float64 и складывает с nTwo; результат — float64

	resultTwo := nOne + int(nTwo)
	// Преобразует nTwo в int (дробная часть отбрасывается!) и складывает с nOne; результат — int

	fmt.Println("Сложение float64(nOne) + nTwo =", resultOne)
	// Выводит результат сложения с сохранением дробной части

	fmt.Println("Сложение nOne + int(nTwo) =", resultTwo)
	// Выводит результат сложения после усечения дробной части у nTwo

	first_name := "Bobik"
	// Объявляет строковую переменную first_name со значением "Bobik"

	var year int32 = 2023
	// Явно объявляет переменную year типа int32 и присваивает год

	iStatus := true
	// Объявляет логическую переменную iStatus со значением true

	var balance float32 = 300.346723
	// Явно объявляет переменную balance типа float32 с заданным значением

	message := fmt.Sprintf("Имя - %s; Год - %d; Статус - %t; Баланc - %.2f", first_name, year, iStatus, balance)
	// Форматирует строку с подстановкой значений: %s — строка, %d — целое число, %t — логическое значение, %.2f — число с плавающей точкой, округлённое до 2 знаков после запятой

	fmt.Println("Сообщение:", message)
	// Выводит сформированное сообщение в консоль

} // Завершает функцию main
